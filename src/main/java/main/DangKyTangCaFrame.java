    public TangCa getModel() throws ParseException {
            
            TangCa tca = new TangCa();
            tca.setId(txtIDCV.getText());
            tca.setMaNV(txtMaNV.getText());
            tca.setTenNV(txtTenNV.getText());
            String batdau = txtGioVao.getText();
            try {
                SimpleDateFormat sdf = new SimpleDateFormat("HH:mm");

                Date gioVao = sdf.parse(batdau);
                if (gioVao != null) {
                    Time gioVaoTime = new Time(gioVao.getTime());
                    tca.setGioVao(gioVaoTime);
                } else {
                    System.err.println("Lỗi: Ngày giờ không hợp lệ");
                    JOptionPane.showMessageDialog(this, "Lỗi: Ngày giờ không hợp lệ");
                }
            } catch (ParseException e) {
                e.printStackTrace();
                System.err.println("Lỗi: Không thể chuyển đổi ngày giờ");
            }

            String ketthuc = txtGioRa.getText();
            try {
                // Sử dụng định dạng "HH:mm" thay vì "HH:mm:ss"
                SimpleDateFormat sdf = new SimpleDateFormat("HH:mm");

                Date gioRa = sdf.parse(ketthuc);
                if (gioRa != null) {
                    // Chuyển đổi Date thành Time
                    Time gioRaTime = new Time(gioRa.getTime());
                    tca.setGioRa(gioRaTime);
                } else {
                    System.err.println("Lỗi: Ngày giờ không hợp lệ");
                    JOptionPane.showMessageDialog(this, "Lỗi: Ngày giờ không hợp lệ");
                }
            } catch (ParseException e) {
                e.printStackTrace();
                System.err.println("Lỗi: Không thể chuyển đổi ngày giờ");
            }


            String ngaynhan = txtNgayNhan.getText();
            try {
                Date nhanviec = dateFormat.parse(ngaynhan);
                if (nhanviec != null) {
                    tca.setNgayNhan(nhanviec);
                } else {
                    System.err.println("Lỗi: Ngày hết hạn không hợp lệ");
                }
            } catch (ParseException e) {
                e.printStackTrace();
            }
            if (rdCatoi.isSelected()) {
                tca.setCaLam(0);
            }
            String thoigianlam = txtThoiGianLam.getText();
            try {
                // Sử dụng định dạng "HH:mm" thay vì "HH:mm:ss"
                SimpleDateFormat sdf = new SimpleDateFormat("HH:mm");

                Date tglam = sdf.parse(thoigianlam);
                if (tglam != null) {
                    // Chuyển đổi Date thành Time
                    Time thoiGianLam = new Time(tglam.getTime());
                    tca.setThoiGianLam(thoiGianLam);
                } else {
                    System.err.println("Lỗi: Ngày giờ không hợp lệ");
                    JOptionPane.showMessageDialog(this, "Lỗi: Ngày giờ không hợp lệ");
                }
            } catch (ParseException e) {
                e.printStackTrace();
                System.err.println("Lỗi: Không thể chuyển đổi ngày giờ");
            }
            
            return tca;
        }
    public boolean validateForm(){
//        if (txtIDCV.getText().isEmpty()) {
//            JOptionPane.showMessageDialog(this, "Id Chưa Nhập");
//            return false;
//        }else 
            if (txtMaNV.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Mã Nhân Viên Chưa Nhập");
            return false;
        }else if (txtTenNV.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Tên NV Chưa Nhập");
            return false;
        }else if (txtNgayNhan.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Chưa có ngày nhận");
            return false;
        }else if (txtGioRa.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Chưa có giờ tan ca");
            return false;
        }else if (txtGioVao.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Chưa có giờ nhận ca");
            return false;
        }else if (txtThoiGianLam.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Chưa có thời gian làm");
            return false;
        }
        return true;
    }   
