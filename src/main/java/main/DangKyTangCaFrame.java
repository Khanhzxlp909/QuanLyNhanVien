    public void fillDataTABLE() {
        model.setRowCount(0); // Xóa tất cả các dòng hiện tại trong bảng
        for (TangCa ls : dao.getTangCaforUser()) {
            Object rowData[] = new Object[8];
            rowData[0] = ls.getId();
            rowData[1] = ls.getMaNV();
            rowData[2] = ls.getTenNV();
            rowData[3] = ls.getNgayNhan();
            if (ls.getCaLam()== 0) {
                rowData[4] = "Đang Làm";
            }  
            rowData[5] = ls.getGioVao();
            rowData[6] = ls.getGioRa();
            rowData[7] = ls.getThoiGianLam();
            
            model.addRow(rowData);
        }
    }
    public void setModel(TangCa tc){
        int row = tbList.getSelectedRow();
        txtIDCV.setText((String) tbList.getValueAt(row, 0));
        txtMaNV.setText((String) tbList.getValueAt(row,1));
        txtTenNV.setText((String) tbList.getValueAt(row, 2));
        Object value = tbList.getValueAt(row, 3);
        if (value != null) {
            if (value instanceof java.sql.Date) {
                java.sql.Date deadlineDate = (java.sql.Date) value;
                SimpleDateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");
                String ngaynhan = dateFormat.format(deadlineDate);

               
                txtNgayNhan.setText(ngaynhan);
            } 
        }
        Object rawValue = (String) tbList.getValueAt(row, 4);
            if (rawValue instanceof Integer) {
            int ca = (int) rawValue;
            tc.setCaLam(ca);
                if(ca == 1 ){
                    rdCatoi.setSelected(true);
                }
            }
        Time gioVao = (Time) tbList.getValueAt(row, 5);
        txtGioVao.setText(sdf.format(gioVao)); 
        Time gioRa  = (Time) tbList.getValueAt(row, 6);
        txtGioRa.setText(sdf.format(gioRa));
        Time tglam  = (Time) tbList.getValueAt(row, 7);
        txtThoiGianLam.setText(sdf.format(tglam));
        
    }
    private void updateTotalGiolam() {
    
        Time giovao = Time.valueOf(txtGioVao.getText() + ":00");
        Time giora = Time.valueOf(txtGioRa.getText() + ":00");

        long milliseconds = giora.getTime() - giovao.getTime();
        Duration duration = Duration.ofMillis(milliseconds);

        long hours = duration.toHours();
        long minutes = duration.minusHours(hours).toMinutes();

        String formattedTime = String.format("%02d:%02d", hours, minutes);
        txtThoiGianLam.setText(formattedTime);
    
}
